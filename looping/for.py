# perulangan (loop)

# for kondisi:
#     aksi

# ini dengan list
angka_list = [0,1,2,3,4]
print(angka_list)

for i in angka_list:
    print(f"i sekarang -> {i}")
print("akhir dari program 1\n")

# ini dengan range

angka_range = range(5)

for i in angka_range:
    print(f"i sekarang -> {i}")
print("akhir dari program 2\n")


angka_range = range(1,5)

for i in angka_range:
    print(f"i sekarang -> {i}")
print("akhir dari program 3\n")

# print bisa bebas
angka_range = range(5)

for i in angka_range:
    print(f"ayo semangat!!!!")
print("akhir dari program 4")

# penggunaan string

data_str = "saya jalan jalan"

for huruf in data_str:
    print(huruf)

print("akhir dari program 5\n")











